    {
        //to show posted info use dd(request('nameoffield'));
        //create a new form submission post
        $formpost = new \App\Post;

        $formpost->name = request('name');
        $formpost->email = request('email');
        $formpost->message = request('message');

        //save the form info to database
        $formpost->save();

        //redirect
        return redirect('/');


